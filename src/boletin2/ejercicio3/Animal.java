package boletin2.ejercicio3;

/**
 * Interfaz que representa las acciones básicas de un animal 
 * definiendo metodos para comer, dormir y hacer ruido.
 */
public interface Animal {
	
	/**
     * Método que representa la funcion de comer de un animal.
     */
	void comer();
	
	/**
     * Método que representa la funcion de dormir de un animal.
     */
	void dormir();
	
	/**
     * Método que representa la funcion de hacer ruido de un animal.
     */
	void hacerRuido();
}